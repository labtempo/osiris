package br.uff.labtempo.osiris.to.common.data;

/**
 *
 * @author Felipe
 */
public abstract class ConsumableTo {

    private final String name;
    private final int value;

    protected ConsumableTo(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }
}
